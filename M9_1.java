class M9_1 {
    public static void main(String[] args) {
        // mainメソッド内でnameメソッドに引数として、変数aと変数bにそれぞれ文字列を代入する。
        name("東京情報クリエイター工学院専門学校", "竹井一馬");
    }


    static void name(String a, String b) {
        System.out.println("学校名：" + a);
        System.out.println("名前：" + b);
    }
}

    

