class M6_1 {
    public static void main(String[] args) {

        // int i = 0で変数iを0で初期化（変数に最初の値を代入する）する。
        // i < 10で変数iに代入された数字が10未満かどうか検査する。
        // i++はiの数値を1づつ増やす。
        // このfor文はiの数値が10になるまで繰り返す。
        for (int i = 0; i < 10; i++) {
            System.out.println("for文のプログラムです");
        }
    }
}
